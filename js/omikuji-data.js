function drawFortune() {
  const weighted = [
    ...Array(3).fill("大吉"),
    ...Array(5).fill("吉"),
    ...Array(7).fill("中吉"),
    ...Array(7).fill("小吉"),
    ...Array(7).fill("末吉"),
    ...Array(5).fill("凶"),
    ...Array(3).fill("大凶")
  ];
  return weighted[Math.floor(Math.random() * weighted.length)];
}

function pickOmikuji(){
  const drawnName=drawFortune();
  const group=omikujiData.find(f=>f.name===drawnName);
  const entry=group.kami[Math.floor(Math.random()*group.kami.length)];
  return {group,entry};
}